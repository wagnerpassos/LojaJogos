    public void atualizaTabela(){
        listaJogadores.clear();
        listaJogadores.addAll(jogadorDAO.read());
        int linha;
        if(linhaAtual != null)
            linha = linhaAtual;
        else
            linha = listaJogadores.size() - 1;
        if(linha >= 0){
            tabelaJogadores.setRowSelectionInterval(linha, linha);
            tabelaJogadores.scrollRectToVisible(
                tabelaJogadores.getCellRect(linha, linha, true));
        }
        linhaAtual = null;
    }
    
    public void atualizaTabela(String busca){
        listaJogadores.clear();
        listaJogadores.addAll(jogadorDAO.read());
        
        for(Iterator<Jogador> iter = listaJogadores.listIterator(); iter.hasNext();){
            Jogador jogador = iter.next();
            if(!jogador.getNome().toLowerCase().contains(busca.toLowerCase()))
                iter.remove();
        }
        int linha = listaJogadores.size() - 1;
        if(linha >= 0){
            tabelaJogadores.setRowSelectionInterval(linha, linha);
            tabelaJogadores.scrollRectToVisible(
                tabelaJogadores.getCellRect(linha, linha, true));
        }
    }
    
    private void trataEdicao(boolean editando){
        botaoCancelar.setEnabled(editando);
        botaoSalvar.setEnabled(editando);
        botaoExcluir.setEnabled(!editando);
        botaoEditar.setEnabled(!editando);
        botaoNovo.setEnabled(!editando);
        botaoFechar.setEnabled(!editando);
        botaoPrimeiro.setEnabled(!editando);
        botaoProximo.setEnabled(!editando);
        botaoAnterior.setEnabled(!editando);
        botaoUltimo.setEnabled(!editando);
        txtNome.setEditable(editando);
        txtApelido.setEditable(editando);
        dropSexo.setEnabled(editando);
    }
    
    public boolean validadorSwing(Jogador jogador){
        Validador validador = new Validador();
        List<String> erros = validador.validarJogador(jogador);
        if(erros.size() > 0)
            ImpressoraSwing.getInstance().impressoraJOP(erros);
        return validador.validarJogador(jogador).size() <= 0;
    }
    
