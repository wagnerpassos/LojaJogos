    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        int linhaSelecionada = tabelaJogadores.getSelectedRow();
        Jogador jogador = listaJogadores.get(linhaSelecionada);
        switch(ImpressoraSwing.getInstance().impressoraConfirmacao("Confirma a operação de exclusão?")){
            case JOptionPane.YES_OPTION:
                if(validadorSwing(jogador)){
                    jogadorDAO.delete(jogador.getId());
                    atualizaTabela();
                }
                break;
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

